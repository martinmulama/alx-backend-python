#!/usr/bin/env python3
""" 2. Basic annotations - floor
"""

import math


def floor(n: float) -> int:
    """
    Takes a float 'n' as an argument and returns its floor as an integer.

    Parameters:
    - n (float): The floating-point number.

    Returns:
    int: The floor of 'n'.
    """
    return math.floor(n)
